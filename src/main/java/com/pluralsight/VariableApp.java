package com.pluralsight;

public class VariableApp {
    public static void main(String[] args) {
        // 1. Declare a variable to store your favorite color
        String favColor;
        favColor = "Red";
        System.out.println("Favorite color is:" + favColor);

        // Declare a variable to store the year you started this class
        int CLASS_YEAR;
        CLASS_YEAR = 2026;
        System.out.println("Class of" + " " + CLASS_YEAR);

        // Declare a variable to store your middle initial
        char MIDDLE_I;
        MIDDLE_I = 'A';
        System.out.println( "Middle Initial is" + " " + MIDDLE_I);
    }
}
